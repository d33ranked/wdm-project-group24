def _proxy(service_topic: str, subpath: str) -> Response:
    """
    Shared handler for all service proxy routes.

    Builds the request envelope, publishes it to the service topic, and
    blocks until the response arrives. The correlation_id attached by
    _attach_correlation_id is used to match the response.

    The Idempotency-Key header is stripped along with hop-by-hop headers
    before forwarding. See _STRIP_HEADERS and the module docstring for
    the full rationale.
    """
    full_path      = f"/{subpath}" if subpath else "/"
    correlation_id = request.correlation_id  # type: ignore[attr-defined]

    # Strip headers that must not reach the services.
    forwarded_headers = {
        k: v for k, v in request.headers
        if k.lower() not in _STRIP_HEADERS
    }

    payload: dict[str, Any] = {
        "method":          request.method,
        "path":            full_path,
        "correlation_id":  correlation_id,
        "query_params":    dict(request.args),
        "headers":         forwarded_headers,
        "body":            request.get_json(silent=True) or dict(request.form) or None,
    }

    try:
        response = kafka_client.send_request(
            topic=service_topic,
            payload=payload,
            correlation_id=correlation_id,
        )
    except RuntimeError as exc:
        logger.error("Kafka send error for topic '%s': %s", service_topic, exc)
        abort(502, description="Service temporarily unavailable")

    return _build_response(response)


def _build_response(response: dict[str, Any]) -> Response:
    """Converts the service response envelope into a Flask Response."""
    status_code: int = response.get("status_code", 200)
    body: Any        = response.get("body", "")
    headers: dict    = response.get("headers") or {}

    if isinstance(body, (dict, list)):
        return jsonify(body), status_code, headers
    return Response(str(body), status=status_code, headers=headers)


@app.before_request
def _attach_correlation_id() -> None:
    """
    Assigns a unique correlation ID to every inbound request.
    This ID travels with the Kafka message and is used to match the
    response back to the correct blocked Flask thread.
    It is distinct from an idempotency key — it has no effect on service
    business logic and is not persisted anywhere.
    """
    request.correlation_id = str(uuid.uuid4())


METHODS = ["GET", "POST", "PUT", "PATCH", "DELETE"]

@app.route("/orders/", defaults={"subpath": ""}, methods=METHODS)
@app.route("/orders/<path:subpath>", methods=METHODS)
def orders_proxy(subpath: str) -> Response:
    return _proxy("gateway.orders", subpath)


@app.route("/stock/", defaults={"subpath": ""}, methods=METHODS)
@app.route("/stock/<path:subpath>", methods=METHODS)
def stock_proxy(subpath: str) -> Response:
    return _proxy("gateway.stock", subpath)


@app.route("/payment/", defaults={"subpath": ""}, methods=METHODS)
@app.route("/payment/<path:subpath>", methods=METHODS)
def payment_proxy(subpath: str) -> Response:
    return _proxy("gateway.payment", subpath)


@app.route("/health")
def health() -> Response:
    return jsonify({"status": "healthy"})

# ---------------------------------------------------------------------------
# Entrypoint
# ---------------------------------------------------------------------------

if __name__ == "__main__":
    ensure_topics(
        KAFKA_BOOTSTRAP_SERVERS,
        GATEWAY_TOPICS + [RESPONSE_TOPIC],
    )

    # KafkaClient starts its response consumer background thread on init.
    # It must be initialised before Flask starts accepting requests, otherwise
    # the first requests have no consumer to receive their responses.
    kafka_client = KafkaClient(KAFKA_BOOTSTRAP_SERVERS)

    app.run(host="0.0.0.0", port=8000, debug=False)